cvcl_result_t cvcl_draw_text(cvcl_image_t  *img,
                              const char    *text,
                              cvcl_i32       x,
                              cvcl_i32       y,
                              cvcl_color_t   color,
                              cvcl_i32       scale) {
    CVCL_CHECK_NULL(img); CVCL_CHECK_NULL(text);
    if (scale < 1) scale = 1;

    cvcl_i32 cx = x;
    for (const char *p = text; *p; p++) {
        unsigned char ch = (unsigned char)*p;
        if (ch == '\n') { cx = x; y += 8 * scale; continue; }
        if (ch < 32 || ch > 127) ch = '?';
        const cvcl_u8 *glyph = font8x8[ch - 32];
        for (cvcl_i32 row = 0; row < 8; row++) {
            for (cvcl_i32 col = 0; col < 8; col++) {
                if (glyph[row] & (1 << col)) {
                    for (cvcl_i32 sy = 0; sy < scale; sy++)
                        for (cvcl_i32 sx = 0; sx < scale; sx++)
                            draw_pixel(img,
                                cx + col*scale + sx,
                                y  + row*scale + sy,
                                color);
                }
            }
        }
        cx += 8 * scale;
    }
    return CVCL_OK;
}
