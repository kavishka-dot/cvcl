/**
 * @file draw.c
 * @brief Drawing primitives: lines, rectangles, circles, text
 *
 * Line: Bresenham's algorithm with thickness
 * Circle: Midpoint circle algorithm
 * Text: Built-in 8x8 bitmap font (ASCII 32-127)
 */

#include <cvcl/cvcl_platform.h>
#include <cvcl/cvcl_draw.h>
#include <cvcl/cvcl_pixel.h>

/* -------------------------------------------------------------------------
 * Color blending helper
 * ---------------------------------------------------------------------- */
static void draw_pixel(cvcl_image_t *img, cvcl_i32 x, cvcl_i32 y,
                        cvcl_color_t col) {
    if (x < 0 || x >= img->width || y < 0 || y >= img->height) return;
    cvcl_u8 r = (cvcl_u8)(col.r * 255.f + 0.5f);
    cvcl_u8 g = (cvcl_u8)(col.g * 255.f + 0.5f);
    cvcl_u8 b = (cvcl_u8)(col.b * 255.f + 0.5f);
    cvcl_u8 a = (cvcl_u8)(col.a * 255.f + 0.5f);
    cvcl_u8 *p = cvcl_pixel_ptr(img, x, y);

    if (img->channels == 1) {
        cvcl_u8 luma = (cvcl_u8)((77*r + 150*g + 29*b) >> 8);
        p[0] = (cvcl_u8)((luma * a + p[0] * (255-a)) >> 8);
    } else if (img->channels == 3) {
        p[0] = (cvcl_u8)((r*a + p[0]*(255-a)) >> 8);
        p[1] = (cvcl_u8)((g*a + p[1]*(255-a)) >> 8);
        p[2] = (cvcl_u8)((b*a + p[2]*(255-a)) >> 8);
    } else if (img->channels == 4) {
        p[0] = (cvcl_u8)((r*a + p[0]*(255-a)) >> 8);
        p[1] = (cvcl_u8)((g*a + p[1]*(255-a)) >> 8);
        p[2] = (cvcl_u8)((b*a + p[2]*(255-a)) >> 8);
        p[3] = 255;
    }
}

static void draw_thick_pixel(cvcl_image_t *img, cvcl_i32 x, cvcl_i32 y,
                               cvcl_color_t col, cvcl_i32 thickness) {
    cvcl_i32 half = thickness / 2;
    for (cvcl_i32 dy = -half; dy <= half; dy++)
        for (cvcl_i32 dx = -half; dx <= half; dx++)
            draw_pixel(img, x+dx, y+dy, col);
}

/* -------------------------------------------------------------------------
 * Line (Bresenham)
 * ---------------------------------------------------------------------- */
cvcl_result_t cvcl_draw_line(cvcl_image_t     *img,
                              cvcl_point2i_t    p0,
                              cvcl_point2i_t    p1,
                              cvcl_color_t      color,
                              cvcl_i32          thickness) {
    CVCL_CHECK_NULL(img); CVCL_CHECK_NULL(img->data);
    if (thickness < 1) thickness = 1;

    cvcl_i32 x0=p0.x, y0=p0.y, x1=p1.x, y1=p1.y;
    cvcl_i32 dx = x1-x0; if (dx<0) dx=-dx;
    cvcl_i32 dy = y1-y0; if (dy<0) dy=-dy;
    cvcl_i32 sx = (x0<x1)?1:-1;
    cvcl_i32 sy = (y0<y1)?1:-1;
    cvcl_i32 err = dx - dy;

    while (1) {
        draw_thick_pixel(img, x0, y0, color, thickness);
        if (x0==x1 && y0==y1) break;
        cvcl_i32 e2 = 2*err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
    return CVCL_OK;
}

/* -------------------------------------------------------------------------
 * Rectangle
 * ---------------------------------------------------------------------- */
cvcl_result_t cvcl_draw_rect(cvcl_image_t  *img,
                              cvcl_rect_t    rect,
                              cvcl_color_t   color,
                              cvcl_i32       thickness,
                              int            filled) {
    CVCL_CHECK_NULL(img); CVCL_CHECK_NULL(img->data);

    cvcl_i32 x0=rect.x, y0=rect.y, x1=rect.x+rect.w-1, y1=rect.y+rect.h-1;

    if (filled) {
        for (cvcl_i32 y=y0; y<=y1; y++)
            for (cvcl_i32 x=x0; x<=x1; x++)
                draw_pixel(img, x, y, color);
        return CVCL_OK;
    }

    cvcl_point2i_t tl={x0,y0}, tr={x1,y0}, bl={x0,y1}, br={x1,y1};
    cvcl_draw_line(img, tl, tr, color, thickness);
    cvcl_draw_line(img, tr, br, color, thickness);
    cvcl_draw_line(img, br, bl, color, thickness);
    cvcl_draw_line(img, bl, tl, color, thickness);
    return CVCL_OK;
}

/* -------------------------------------------------------------------------
 * Circle (Midpoint algorithm)
 * ---------------------------------------------------------------------- */
static void circle_points(cvcl_image_t *img, cvcl_i32 cx, cvcl_i32 cy,
                            cvcl_i32 x, cvcl_i32 y, cvcl_color_t col,
                            cvcl_i32 t, int filled) {
    if (filled) {
        for (cvcl_i32 i=-x; i<=x; i++) {
            draw_pixel(img, cx+i, cy+y, col);
            draw_pixel(img, cx+i, cy-y, col);
        }
        for (cvcl_i32 i=-y; i<=y; i++) {
            draw_pixel(img, cx+i, cy+x, col);
            draw_pixel(img, cx+i, cy-x, col);
        }
    } else {
        draw_thick_pixel(img, cx+x, cy+y, col, t);
        draw_thick_pixel(img, cx-x, cy+y, col, t);
        draw_thick_pixel(img, cx+x, cy-y, col, t);
        draw_thick_pixel(img, cx-x, cy-y, col, t);
        draw_thick_pixel(img, cx+y, cy+x, col, t);
        draw_thick_pixel(img, cx-y, cy+x, col, t);
        draw_thick_pixel(img, cx+y, cy-x, col, t);
        draw_thick_pixel(img, cx-y, cy-x, col, t);
    }
}

cvcl_result_t cvcl_draw_circle(cvcl_image_t  *img,
                                cvcl_point2i_t center,
                                cvcl_i32       radius,
                                cvcl_color_t   color,
                                cvcl_i32       thickness,
                                int            filled) {
    CVCL_CHECK_NULL(img); CVCL_CHECK_NULL(img->data);
    if (radius <= 0) return CVCL_OK;

    cvcl_i32 x=0, y=radius, d=1-radius;
    while (x <= y) {
        circle_points(img, center.x, center.y, x, y, color, thickness, filled);
        if (d < 0) d += 2*x+3;
        else       { d += 2*(x-y)+5; y--; }
        x++;
    }
    return CVCL_OK;
}

/* -------------------------------------------------------------------------
 * Polyline
 * ---------------------------------------------------------------------- */
cvcl_result_t cvcl_draw_polyline(cvcl_image_t         *img,
                                  const cvcl_point2i_t *pts,
                                  cvcl_i32              n_pts,
                                  cvcl_color_t          color,
                                  cvcl_i32              thickness,
                                  int                   closed) {
    CVCL_CHECK_NULL(img); CVCL_CHECK_NULL(pts);
    CVCL_CHECK_ARG(n_pts >= 2);

    for (cvcl_i32 i = 0; i < n_pts-1; i++)
        cvcl_draw_line(img, pts[i], pts[i+1], color, thickness);
    if (closed)
        cvcl_draw_line(img, pts[n_pts-1], pts[0], color, thickness);
    return CVCL_OK;
}

/* -------------------------------------------------------------------------
 * Blit (image-on-image paste with alpha)
 * ---------------------------------------------------------------------- */
cvcl_result_t cvcl_draw_image(cvcl_image_t       *img,
                               const cvcl_image_t *src_patch,
                               cvcl_i32            x,
                               cvcl_i32            y) {
    CVCL_CHECK_NULL(img); CVCL_CHECK_NULL(src_patch);

    for (cvcl_i32 py=0; py<src_patch->height; py++) {
        cvcl_i32 dy = y + py;
        if (dy < 0 || dy >= img->height) continue;
        const cvcl_u8 *sr = cvcl_image_row(src_patch, py);
        for (cvcl_i32 px=0; px<src_patch->width; px++) {
            cvcl_i32 dx = x + px;
            if (dx < 0 || dx >= img->width) continue;
            cvcl_color_t col;
            col.r = sr[px*src_patch->channels+0] / 255.f;
            col.g = src_patch->channels>=3 ? sr[px*src_patch->channels+1]/255.f : col.r;
            col.b = src_patch->channels>=3 ? sr[px*src_patch->channels+2]/255.f : col.r;
            col.a = src_patch->channels==4 ? sr[px*4+3]/255.f : 1.f;
            draw_pixel(img, dx, dy, col);
        }
    }
    return CVCL_OK;
}

